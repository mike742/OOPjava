
public class Auto {

	private String make;
	private String color;
	
	public void setMake(String value) {
		make = value;
	}
	
	public String getMake() {
		return make;
	}
	
	public void setColor(String value) {
		color = value;
	}
	
	public void printMake() {
		System.out.print(make);
	}
	
	public void printColor() {
		System.out.print(color);
	}
}
